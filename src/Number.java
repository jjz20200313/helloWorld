
public class Number {

    private double x=1.0;

    private double y=2.0;

    public void setX(double val1){
        x=val1;
    }

    public void setY(double val2){
        y=val2;
    }

    public double getX(){
        return x;
    }

    public double getY(){
        return y;
    }

    public double add(){
        return x+y;
    }


}
